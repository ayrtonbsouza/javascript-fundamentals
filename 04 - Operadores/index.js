/**
 * Operadores Aritméticos:
 * Adição: +
 * Subtração: -
 * Multiplicação: *
 * Divisão: /
 * Resto da Divisão: %
 * Exponenciação: **
 */

/**
 * Operadores Lógicos
 * Menor que: <
 * Maior que: >
 * Igual à: ===
 * Igual à (desconsiderando tipo): ==
 * Diferente de: !==
 * Diferente de (desconsiderando tipo): !=
 * Menor ou igual à: <=
 * Maior ou igual à: >=
 * Ou: ||
 * E: &&
 */

 console.log('\n *** Operações Aritméticas *** \n');
 console.log('1 + 1 =', 1+1);
 console.log('1 - 1 =', 1-1);
 console.log('1 * 1 =', 1*1);
 console.log('1 / 1 =', 1/1);
 console.log('9 % 2 =', 9%2);
 console.log('3 ** 2 =', 3**2);
 console.log('\n *** Operações Lógicas *** \n');
 console.log('1 < 2: ', 1<2);
 console.log('2 > 1: ', 2>1);
 console.log('2 === 2: ', 2===2);
 console.log('2 !== 3: ', 2!==3);
 console.log('2 <= 3: ', 2<=3);
 console.log('2 >= 2: ', 2>=2);